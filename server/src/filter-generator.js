export function generateReviewFilter({ snapshotId, generatedAt = new Date().toISOString() }) {
  const name = `Deck Companion Review ${snapshotId}`;
  const description = `Generated by Last Epoch Deck Companion at ${generatedAt}. Conservative filter: it only shows/emphasizes valuable rarities and never hides items.`;
  return `<?xml version="1.0" encoding="utf-8"?>
<ItemFilter xmlns:i="http://www.w3.org/2001/XMLSchema-instance">
  <name>${escapeXml(name)}</name>
  <filterIcon>17</filterIcon>
  <filterIconColor>3</filterIconColor>
  <description>${escapeXml(description)}</description>
  <lastModifiedInVersion>1.0</lastModifiedInVersion>
  <lootFilterVersion>0</lootFilterVersion>
  <rules>
    <Rule>
      <type>SHOW</type>
      <conditions>
        <Condition i:type="RarityCondition">
          <rarity>UNIQUE SET EXALTED</rarity>
        </Condition>
      </conditions>
      <color>0</color>
      <isEnabled>true</isEnabled>
      <levelDependent>false</levelDependent>
      <minLvl>0</minLvl>
      <maxLvl>0</maxLvl>
      <emphasized>true</emphasized>
      <nameOverride />
    </Rule>
  </rules>
</ItemFilter>
`;
}

function escapeXml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll("\"", "&quot;")
    .replaceAll("'", "&apos;");
}
